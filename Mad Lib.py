def madLibs():
    noun = input("Type a noun")
    verb = input("Type a past-tense verb")
    adverb = input("Type an adverb")
    adjective = input("Type and adjective")
    noun2 = input("Type another noun")

    sentance = noun + " " + " " + verb + " " + adverb + " into a " + adjective + " " + noun2 + "."
    
    print(sentance)
